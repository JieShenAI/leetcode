from typing import List


class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        row = []
        col = []
        area = [[] for i in range(9)]
        for i in range(9):
            for j in range(9):
                if board[i][j] != '.':
                    row.append(board[i][j])
                    idx = int((i // 3) * 3 + j // 3)
                    area[idx].append(board[i][j])
                if board[j][i] != '.':
                    col.append(board[j][i])

            row_set = set(row)
            if len(row_set) != len(row):
                return False
            col_set = set(col)
            if len(col_set) != len(col):
                return False
            row = []
            col = []
        for a in area:
            if len(a) != len(set(a)):
                return False
        return True


