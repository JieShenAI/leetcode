import queue
from collections import deque
from typing import List


class Solution:
    def secondMinimum(self, n: int, edges: List[List[int]], time: int, change: int) -> int:
        # å…ˆ
        graph = [[] for _ in range(n + 1)]  # å¤šå¼€è¾Ÿç©ºé—´ï¼Œå› ä¸ºæœ€å°å€¼ä»1å¼€å§‹
        # å¡«å……graph
        for e in edges:
            x, y = e[0], e[1]
            # ç”±é¢˜æ„ï¼š
            #   edges[i] = [ui, vi] è¡¨ç¤ºä¸€æ¡èŠ‚ç‚¹ ui å’ŒèŠ‚ç‚¹ vi ä¹‹é—´çš„åŒå‘è¿é€šè¾¹ã€‚
            #   æ¯ç»„èŠ‚ç‚¹å¯¹ç”± æœ€å¤šä¸€æ¡ è¾¹è¿é€š
            # ä¸ä¼šå‡ºç°: a->b,b->a; åªä¼šæœ‰a->bæˆ–b->aï¼›
            # æ‰€ä»¥å¯ä»¥åœ¨åˆ—è¡¨ä¸­æ”¾å¿ƒçš„appendï¼Œä¸ç”¨æ‹…å¿ƒé‡å¤
            graph[x].append(y)
            graph[y].append(x)

        # dist[i,0]: ä»1->içš„æœ€çŸ­è·¯å¾„é•¿åº¦
        # dist[i,1]: ä»1->içš„æ¬¡çŸ­è·¯å¾„é•¿åº¦
        dist = [[float('inf')] * 2 for _ in range(n + 1)]
        # dequeå­˜å‚¨çš„æ˜¯[i,distance],ä»1->içš„è·¯å¾„é•¿åº¦
        start = [1, 0]
        q = deque()
        # q = queue.Queue()
        q.append(start)
        while dist[n][1] == float('inf'):
            q_pop = q.popleft()
            # q_pop:[i,distance]
            distance = q_pop[1] + 1
            for pos_x in graph[q_pop[0]]:
                if distance < dist[pos_x][0]:
                    dist[pos_x][0] = distance
                    q.append((pos_x, distance))
                elif dist[pos_x][0] < distance < dist[pos_x][1]:
                    dist[pos_x][1] = distance
                    q.append((pos_x, distance))
                # æ— æ³•æ›´æ–°è·ç¦»çš„èŠ‚ç‚¹ä¸å†åŠ å…¥é˜Ÿåˆ—
        t = 0
        for _ in range(dist[n][1]):
            tmp = t % (2 * change)
            # çº¢è·¯ç¯å¡ç‚¹ï¼Œä¸èƒ½é€šè¿‡
            if tmp >= change:
                t += 2 * change - tmp
            t += time
        return t


