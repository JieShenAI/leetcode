# Definition for a binary tree node.
class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right


class Solution:
    def levelOrder(self, root: TreeNode) -> List[List[int]]:
        res = []
        q = deque()
        if root is not None:
            q.append(root)
        while q:
            Size = len(q)
            tmp = []
            while Size > 0:
                node = q.popleft()
                tmp.append(node.val)
                if node.left is not None:
                    q.append(node.left)
                if node.right is not None:
                    q.append(node.right)
                Size -= 1
            res.append(tmp)
        return res
